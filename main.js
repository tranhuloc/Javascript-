/*
Giới thiệu về toán tử trong JS
1. Toán tử số học - Arithmetic
2. Toán tử gán - Assignment
3. Toán tử so sánh - Comparison
4. Toán tử logic - Logical
*/

//1. Toán tử số học - Arithmetic
var a = 1 + 2;
console.log(a);

//2. Toán tử gán - Assignment => =
var fullName = "Tran Huu Loc";
console.log(fullName);

//3. Toán tử so sánh - Comparison => (>, <, >=, <=, ==)
var a = 1;
var b = 2;

if (a == b) {
    alert("Dung");
}

//4. Toán tử logic - Logical
var a = 1;
var b = 2;

if (a > 0 && b > 0) {
    alert('a & b lon hon 0')
}